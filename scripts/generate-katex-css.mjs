import fsSync from "node:fs";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const katexRoot = path.join(repoRoot, "node_modules", "katex", "dist");
const katexCssPath = path.join(katexRoot, "katex.min.css");
const katexFontsDir = path.join(katexRoot, "fonts");
const outputPath = path.join(repoRoot, "src", "lib", "generated", "katex-css-embedded.ts");

function toDataUri(bytes) {
  return `data:font/woff2;base64,${Buffer.from(bytes).toString("base64")}`;
}

async function main() {
  let css = await fs.readFile(katexCssPath, "utf8");

  css = css.replace(
    /url\(fonts\/([^)]+?\.woff2)\)/g,
    (_, fileName) => {
      const fontPath = path.join(katexFontsDir, fileName);
      const fontBytes = fsSync.readFileSync(fontPath);
      return `url(${toDataUri(fontBytes)})`;
    },
  );

  css = css.replace(
    /,\s*url\(fonts\/[^)]+?\.woff\)\s*format\("woff"\)\s*,\s*url\(fonts\/[^)]+?\.ttf\)\s*format\("truetype"\)/g,
    "",
  );

  if (css.includes("url(fonts/")) {
    throw new Error("KaTeX CSS still contains unresolved relative font URLs.");
  }

  const fileContents = [
    "// Generated by scripts/generate-katex-css.mjs",
    "// Regenerate when the KaTeX package version changes.",
    "",
    `export const katexCssEmbedded = ${JSON.stringify(css)};`,
    "",
  ].join("\n");

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, fileContents, "utf8");
}

await main();
